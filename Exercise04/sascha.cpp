int main() {
	bool i;
	double j = 1;

	i = 1;

	while(i == j) {
		i = 0;
	}

        while(i != j) {
	        i = 0;
        }

        while(i > j) {
	        i = 0;
        }

        while(i < j) {
	        i = 0;
        }

        while(i <= j) {
	        i = 0;
        }

        while(i >= j) {
	        i = 0;
        }

	i = i + 1;
	i = i + i;
	i = i + j;

        i = i - 1;
        i = i - i;
        i = i - j;

        i = i * 1;
        i = i * i;
        i = i * j;

        i = i / 1;
        i = i / i;
        i = i / j;



return 1;
}
